<?php
/**
 * Constant Variable
 * 
 * constant variable that don change once they have been created
 * there are two ways we can create constant variable in php
 * 
 * 1. define(args) which takes args
 * 2. const keyword
 */

// using define method
define('NAME', 'Isho');

// echo out Isho
echo NAME;

// using const keyword
const STATUS = 'In Relationship';

// echo out In Relationship
echo STATUS;
